#ifndef PAGER_H
#define PAGER_H

#define X86_PAGER_BITMASK_PRESENT 1 << 0
#define X86_PAGER_BITMASK_WRITABLE 1 << 1
#define X86_PAGER_BITMASK_USER 1 << 2
#define X86_PAGER_BITMASK_WRITE_THROUGH 1 << 3
#define X86_PAGER_BITMASK_CACHE_DISABLE 1 << 4
#define X86_PAGER_BITMASK_ACCESSED 1 << 5
#define X86_PAGER_BITMASK_AVAILABLE 1 << 6
#define X86_PAGER_BITMASK_PAGE_SIZE 1 << 7
#define X86_PAGER_BITMASK_AVAILABLE2 1 << 8 || 1 << 9 || 1 << 10 || 1 << 11
#define X86_PAGER_BITMASK_ADDR 0x000FFFFFFFFFF000
#define X86_PAGER_BITMASK_AVAILABLE3 0x7FF0000000000000
#define X86_PAGER_BITMASK_XD 1 << 63

#define PML4_GET_ADDR(VALUE) ((VALUE) & X86_PAGER_BITMASK_ADDR)
#define PML4_GET_FLAGS(VALUE) ((VALUE) & ~X86_PAGER_BITMASK_ADDR)

#endif